export function resolveLanguage(language: Language): Exclude<Language, 'system'> {
  if (language !== 'system') return language;
  const locale = typeof navigator !== 'undefined' ? navigator.language.toLowerCase() : 'en';
  if (locale.startsWith('id')) return 'id';
  if (locale.startsWith('ja')) return 'ja';
  return 'en';
}

export function createTranslator(language: Language) {
  const resolvedLanguage = resolveLanguage(language);
  const dictionary = dictionaries[resolvedLanguage];
  return (key: string) => dictionary[key] || dictionaries.en[key] || key;
}
